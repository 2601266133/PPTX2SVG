package com.cisco.pptx_to_jpg_converter.xslfchart.attribute;

public class XFrame {

	double offX;
	double offY;
	double width;
	double height;

	public double getOffX() {
		return offX;
	}

	public void setOffX(double offX) {
		this.offX = offX;
	}

	public double getOffY() {
		return offY;
	}

	public void setOffY(double offY) {
		this.offY = offY;
	}

	public double getWidth() {
		return width;
	}

	public void setWidth(double width) {
		this.width = width;
	}

	public double getHeight() {
		return height;
	}

	public void setHeight(double height) {
		this.height = height;
	}

}
