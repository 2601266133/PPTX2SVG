package com.cisco.pptx_to_jpg_converter.xslfchart.attribute;

public class TextParagraphs {

	int baseline;
	String spc;
	String kern;
	String strike;
	String u;
	String i;
	boolean b;
	String sz;
	String endParaRPr;

	String fillType;

	public int getBaseline() {
		return baseline;
	}

	public void setBaseline(int baseline) {
		this.baseline = baseline;
	}

	public String getSpc() {
		return spc;
	}

	public void setSpc(String spc) {
		this.spc = spc;
	}

	public String getKern() {
		return kern;
	}

	public void setKern(String kern) {
		this.kern = kern;
	}

	public String getStrike() {
		return strike;
	}

	public void setStrike(String strike) {
		this.strike = strike;
	}

	public String getU() {
		return u;
	}

	public void setU(String u) {
		this.u = u;
	}

	public String getI() {
		return i;
	}

	public void setI(String i) {
		this.i = i;
	}

	public boolean getB() {
		return b;
	}

	public void setB(boolean b) {
		this.b = b;
	}

	public String getSz() {
		return sz;
	}

	public void setSz(String sz) {
		this.sz = sz;
	}

	public String getFillType() {
		return fillType;
	}

	public void setFillType(String fillType) {
		this.fillType = fillType;
	}

	public String getEndParaRPr() {
		return endParaRPr;
	}

	public void setEndParaRPr(String endParaRPr) {
		this.endParaRPr = endParaRPr;
	}

}
