package com.cisco.pptx_to_jpg_converter.xslfchart.attribute;

/**
 * 
 * 5.1.5.2.11 spcPts (Spacing Points)
 * 
 * 
 * This element specifies the amount of white space that is to be used between
 * lines and paragraphs in the form of a text point size. The size is specified
 * using points where 100 is equal to 1 point font and 1200 is equal to 12 point
 *
 */

public class SpacingPoints {

	int val;

	public int getVal() {
		return val;
	}

	public void setVal(int val) {
		this.val = val;
	}

}
